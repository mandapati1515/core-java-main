public class ArithMeticOpsExample {
    public static void main(String args[]) {
        int res1, res2, res3;
        int a = 10, b = 30;
        res1 = (a + b) / 2;
        res2 = (a * b) % 5;
        res3 = a - b;
        System.out.println("===============================");
        System.out.println("res1 is " + res1);
        System.out.println("res2 is " + res2);
        System.out.println("res3 is " + res3);
        System.out.println("===============================");
    }
}